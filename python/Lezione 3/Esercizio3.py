# Dati del triangolo
lato1 = 9
lato2 = 12
lato3 = 15

# Calcolo perimetro
perimetro = lato1 + lato2 + lato3

# Calcolo area (triangolo rettangolo)
area = lato1 * lato2 /2

# Verifica Teorema di Pitagora
teorema_pitagora = lato3**2 == lato1**2 + lato2**2

# Risultati
print("Perimetro:", perimetro)
print("Area:", area)
print("Teorema di Pitagora valido:", teorema_pitagora)
