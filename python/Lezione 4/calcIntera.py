
print("Calcolatrice per numeri interi")

# Richiesta dei numeri
num1 = int(input("Inserisci il primo numero: "))
num2 = int(input("Inserisci il secondo numero: "))

# Calcolo delle operazioni
risultato_addizione = num1 + num2
risultato_sottrazione = num1 - num2
risultato_moltiplicazione = num1 * num2
risultato_divisione = num1 / num2

# Potenza
risultato_potenza1 = num1**2
risultato_potenza2 = num2**2

# Stampa dei risultati
print(f"Addizione: {risultato_addizione}")
print(f"Sottrazione: {risultato_sottrazione}")
print(f"Moltiplicazione: {risultato_moltiplicazione}")
print(f"Divisione: {risultato_divisione}")
print(f"Potenza del primo numero: {risultato_potenza1}")
print(f"Potenza del secondo numero: {risultato_potenza2}")
